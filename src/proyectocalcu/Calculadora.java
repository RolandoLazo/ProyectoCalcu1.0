    public static String Calculadora(String M1, String M2, String Signo) {
        Double Resultado = 0.0;
        String Respuesta;
        if (Signo.equals("+")) {
            Resultado = Double.parseDouble(M1) + Double.parseDouble((M2));
        }
        if (Signo.equals("-")) {
            Resultado = Double.parseDouble(M1) - Double.parseDouble((M2));
        }
        if (Signo.equals("*")) {
            Resultado = Double.parseDouble(M1) * Double.parseDouble((M2));
        }
        if (Signo.equals("/")) {
            Resultado = Double.parseDouble(M1) / Double.parseDouble((M2));
        }
        Respuesta = Resultado.toString();
        return Respuesta;
    }
